def listar_moedas():

    while True:

        print("\n" + "~"*30 + " Moedas Disponíveis " + "~"*30)
        print("\n* 1 - Listar Todas as Moedas Disponíveis")
        print("* 2 - Consultar Moeda por País")
        print("* 3 - Consultar Moeda por Código")
        print("* 4 - Voltar ao Menu Anterior")

        escolha = input("\nDigite a opção desejada: ")

        if escolha == '1':
            for codigo, info in moedas.items():
                print(f"{codigo}: {info['nome']} - {info['país']}")
            continue

        elif escolha == '2':
            pais = input("Digite o nome do país: ")

            for codigo, info in moedas.items():
                if info['país'].lower() == pais.lower():
                    print(f"{codigo}: {info['nome']} - {info['país']}")
                    continue
            else:
                print("País não encontrado.")
                continue

        elif escolha == '3':
            codigo = input("Digite o código da moeda (ex: USD, EUR): ").upper()

            if codigo in moedas:
                info = moedas[codigo]
                print(f"{codigo}: {info['nome']} - {info['país']}")
                continue
            else:
                print("Código de moeda não encontrado.")
                continue

        elif escolha == '4':
            break

        else:
            print("Opção inválida. Tente novamente.")
            continue